/*
    Increment & Decrement Operators
    ++      --  
*/


var a:number
var b:number

a = 5
b = a++    //Post Increment
console.log("a = ",a,"\tb = ",b) //a=5,b=6

a = 5
b = ++a    //Pre Increment
console.log("a = ",a,"\tb = ",b) //a=5,b=6

a = 5
b  = a--    //Post Decrement
console.log("a = ",a,"\tb = ",b) //a=5,b=4

a = 5
b = --a    //pre Decrement
console.log("a = ",a,"\tb = ",b) //a=5,b=4
